class A:
    def ping(self):
        print('ping:', self)


class B(A):
    def pong(self):
        print('pong:', self)


class C(A):
    def pong(self):
        print('PONG:', self)


class D(C, B):
    def ping(self):
        # super().ping()
        A.ping(self)
        print('post-ping:', self)

    def pingpong(self):
        self.ping()
        super().ping()
        self.pong()
        super().pong()
        B.pong(self)


d = D()
d.pong()
print(D.__mro__)
d.pingpong()

import tkinter as tk

